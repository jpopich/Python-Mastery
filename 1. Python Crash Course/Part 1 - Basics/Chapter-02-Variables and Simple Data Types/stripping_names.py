# 2.7 Stripping Names

name = "\n    BigMac    \t"
print("white_space->" + name + "<-white_space")

print("\n")
print("-----------------------------------")
print("Using lstrip():")
print("white_space->" + name.lstrip() + "<-white_space")
print("\n")

print("Using rstrip():")
print("white_space->" + name.rstrip() + "<-white_space")
print("\n")

print("Using strip():")
print("white_space->" + name.strip() + "<-white_space")




